async def fetch_movie_poster(title: str, year: Optional[int] = None) -> Optional[bytes]:
    """рдореВрд╡реА рдкреЛрд╕реНрдЯрд░ рдлреЗрдЪ рдХрд░реЗрдВ"""
    try:
        params = {"title": clean_text(title)}
        if year:
            params["year"] = str(year)
            
        async with aiohttp.ClientSession() as session:
            async with session.get(POSTER_API_URL, params=params, timeout=10) as response:
                if response.status == 200:
                    return await response.read()
                print(f"тЪая╕П рдкреЛрд╕реНрдЯрд░ API рдПрд░рд░: HTTP {response.status}")
    except (aiohttp.ClientError, asyncio.TimeoutError) as e:
        print(f"ЁЯМР рдиреЗрдЯрд╡рд░реНрдХ рдПрд░рд░: {type(e).__name__}")
    return None
